"""Генерация рерайта.

Модель подключается через vsellm (OpenAI-совместимый API); какая именно модель — задаёт
LLM_MODEL в настройках. Без заданного ключа работает заглушка, чтобы сервис оставался
рабочим для демо. Проверка уникальности (Text.ru) — задел на будущее (пока uniqueness=None).
"""
import logging
from typing import Optional

from database.config import get_settings

logger = logging.getLogger(__name__)

# Промпт задаёт tone of voice редакции и требование не выдумывать факты.
# Правится под редакцию; при желании выносится в отдельный конфиг по аналогии с region.yml.
REWRITE_SYSTEM_PROMPT = """Ты — профессиональный журналист регионального телеканала (Севастополь и Крым). Пиши с позиции регионального журналиста, жителя Севастополя. Перепиши присланный инфоповод в статью для новостного сайта. Уникальность текста — не ниже 80%.

Сначала выдели главные смысловые моменты новости и обязательно используй их в тексте.

ФОРМАТ ОТВЕТА:
- Первая строка — заголовок.
- Дальше с новой строки — текст статьи абзацами.
- Никаких служебных пометок, пояснений и разметки.

ЗАГОЛОВОК:
- точно отражает смысл статьи и задаёт её тему (в тексте нет ничего, чего нет в заголовке);
- яркий и вовлекающий, но без кликбейта и восторженных эпитетов;
- без штампов, клише и канцелярита;
- без двоеточий.

ТЕКСТ СТАТЬИ:
- объём примерно как в исходнике, но не более 2500 знаков;
- дели на абзацы; принцип «перевёрнутой пирамиды» — суть (кто, что, где, когда) в первом абзаце, дальше детали и контекст;
- один абзац — одна мысль (3–5 предложений), первое предложение задаёт содержание абзаца;
- используй только факты из исходника; не выдумывай факты, цифры, имена, должности и даты; не хватает данных — не додумывай;
- прямые цитаты приводи слово в слово, обязательно с именем и должностью спикера;
- не пиши выводов и морали в конце;
- избегай штампов вроде «Севастополь оказался в центре внимания».

ИНФОРМАЦИОННЫЙ СТИЛЬ (по Ильяхову):
- полезное действие: пиши честно и по делу, помогай читателю понять событие, не манипулируй;
- факты вместо оценок: не «крупный пожар», а «горело 300 кв. м» — показывай делом, а не эпитетами;
- убирай канцелярит: причастия, деепричастия и отглагольные существительные заменяй простыми глаголами («осуществил» → «сделал», «являлся» → «был», «в рамках проведения» → «во время»);
- активный залог вместо страдательного: «власти открыли», а не «было открыто»;
- убирай стоп-слова: вводные обороты («стоит отметить», «как известно», «на самом деле», «таким образом»), усилители («очень», «крайне», «самый») и оценочные прилагательные без фактов.

УНИКАЛЬНОСТЬ (обязательно, цель ≥ 80%):
- перефразируй и переструктурируй изложение своими словами; не копируй фразы исходника дословно (кроме цитат);
- варьируй длину предложений — от 6–8 до 18–20 слов, чередуй ритм и форму, допускай сложноподчинённые конструкции;
- объединяй короткие фразы в более длинные, где уместно (не телеграфный стиль);
- не повторяй однокоренные слова чаще двух раз в абзаце;
- в каждом абзаце давай не только факт, но и контекст: цифру, сравнение, историю, уточнение;
- следи за связностью — каждый абзац логически вытекает из предыдущего."""


# Промпт доработки: правит ТЕКУЩИЙ вариант по указанию редактора, сохраняя факты и инфостиль.
REFINE_PROMPT = """Ты — редактор регионального новостного СМИ (инфостиль по Ильяхову). Тебе дают ИСХОДНИК, ТЕКУЩИЙ вариант статьи и УКАЗАНИЕ редактора. Доработай ТЕКУЩИЙ текст строго по указанию.

Правила:
- Выполни указание редактора, остальное по возможности сохрани.
- Не выдумывай факты — бери только из исходника. Прямые цитаты приводи дословно с именем и должностью спикера.
- Держи инфостиль: без канцелярита, активный залог, факты вместо оценок, высокая уникальность.
- Формат ответа: первая строка — заголовок, дальше текст статьи абзацами. Без пояснений и разметки."""


def refine_rewrite(title: str, body: str, current: str, instruction: str) -> str:
    """Дорабатывает текущий вариант статьи по указанию редактора. Без ключа возвращает как есть."""
    settings = get_settings()
    if not settings.LLM_API_KEY:
        return current

    from openai import OpenAI

    client = OpenAI(api_key=settings.LLM_API_KEY, base_url=settings.LLM_BASE_URL)
    user = (
        f"ИСХОДНИК:\nЗаголовок: {title}\n\n{body}\n\n---\n"
        f"ТЕКУЩИЙ ВАРИАНТ:\n{current}\n\n---\n"
        f"УКАЗАНИЕ РЕДАКТОРА:\n{instruction}"
    )
    response = client.chat.completions.create(
        model=settings.LLM_MODEL,
        messages=[
            {"role": "system", "content": REFINE_PROMPT},
            {"role": "user", "content": user},
        ],
        temperature=settings.LLM_TEMPERATURE,
    )
    return (response.choices[0].message.content or current).strip() or current


def _stub(title: str, body: str) -> tuple[str, Optional[float]]:
    """Резервная генерация без LLM: компонует заголовок и текст в черновик."""
    title = (title or "").strip().rstrip(".")
    body = (body or "").strip()
    rewritten = f"{title}.\n\n{body}" if body else f"{title}."
    return rewritten, None


def generate_rewrite(title: str, body: str) -> tuple[str, Optional[float]]:
    """Возвращает (текст рерайта, % уникальности).

    Если ключ LLM не задан — заглушка. Иначе вызывает модель через vsellm.
    Ошибка вызова пробрасывается наверх (воркер пометит задачу статусом error).
    """
    settings = get_settings()
    if not settings.LLM_API_KEY:
        return _stub(title, body)

    from openai import OpenAI

    client = OpenAI(api_key=settings.LLM_API_KEY, base_url=settings.LLM_BASE_URL)
    response = client.chat.completions.create(
        model=settings.LLM_MODEL,
        messages=[
            {"role": "system", "content": REWRITE_SYSTEM_PROMPT},
            {"role": "user", "content": f"ИСХОДНЫЙ ИНФОПОВОД.\nЗаголовок источника: {title}\n\nТекст источника:\n{body}"},
        ],
        temperature=settings.LLM_TEMPERATURE,
    )
    text = response.choices[0].message.content or ""
    logger.info("Рерайт получен от модели %s, %d символов", settings.LLM_MODEL, len(text))
    return text, None
