"""Генерация рерайта.

Модель подключается через vsellm (OpenAI-совместимый API); какая именно модель — задаёт
LLM_MODEL в настройках. Без заданного ключа работает заглушка, чтобы сервис оставался
рабочим для демо. Проверка уникальности (Text.ru) — задел на будущее (пока uniqueness=None).
"""
import logging
from typing import Optional

from database.config import get_settings

logger = logging.getLogger(__name__)

# Промпт задаёт tone of voice редакции и требование не выдумывать факты.
# Правится под редакцию; при желании выносится в отдельный конфиг по аналогии с region.yml.
REWRITE_SYSTEM_PROMPT = """Ты — выпускающий редактор регионального новостного СМИ (Севастополь и Крым). Перепиши инфоповод в готовую к публикации заметку в информационном стиле (по Максиму Ильяхову).

ЧТО ВЫДАТЬ:
- Первая строка — заголовок: конкретный, по сути события, без кликбейта и общих слов.
- Дальше — текст заметки абзацами. Объём примерно как в исходнике.
- Структура «перевёрнутая пирамида»: первый абзац — суть (кто, что, где, когда), затем детали и контекст.

ИНФОРМАЦИОННЫЙ СТИЛЬ (главное):
- Долой канцелярит: причастия, деепричастия и отглагольные существительные заменяй простыми глаголами («осуществил» → «сделал», «являлся» → «был», «в рамках проведения» → «во время»).
- Активный залог вместо страдательного: «власти открыли», а не «было открыто».
- Убирай стоп-слова: вводные обороты («стоит отметить», «как известно», «на самом деле», «таким образом»), усилители («очень», «крайне», «самый») и оценочные прилагательные без фактов («крупный», «серьёзный», «эффективный»).
- Факты вместо оценок: не «большой пожар», а «горело 300 кв. м». Показывай делом, а не эпитетами.
- Короткие предложения, одна мысль — одно предложение. Без штампов, клише, тавтологии и слов-паразитов.

ФАКТЫ (критично):
- Не придумывай факты, цифры, имена, должности, даты и цитаты — бери только из исходника. Не хватает данных — не додумывай.
- Сохрани все конкретные детали: даты, места, числа, должности, названия.
- Прямые цитаты приводи дословно (можно сократить, но не переписывать слова спикера).

УНИКАЛЬНОСТЬ:
- Перефразируй и переструктурируй изложение своими словами, не копируй фразы исходника дословно (кроме цитат) — текст должен быть уникальным.

ФОРМАТ ОТВЕТА:
- Первая строка — заголовок, дальше с новой строки — текст заметки абзацами. Без служебных пометок, пояснений и разметки."""


def _stub(title: str, body: str) -> tuple[str, Optional[float]]:
    """Резервная генерация без LLM: компонует заголовок и текст в черновик."""
    title = (title or "").strip().rstrip(".")
    body = (body or "").strip()
    rewritten = f"{title}.\n\n{body}" if body else f"{title}."
    return rewritten, None


def generate_rewrite(title: str, body: str) -> tuple[str, Optional[float]]:
    """Возвращает (текст рерайта, % уникальности).

    Если ключ LLM не задан — заглушка. Иначе вызывает модель через vsellm.
    Ошибка вызова пробрасывается наверх (воркер пометит задачу статусом error).
    """
    settings = get_settings()
    if not settings.LLM_API_KEY:
        return _stub(title, body)

    from openai import OpenAI

    client = OpenAI(api_key=settings.LLM_API_KEY, base_url=settings.LLM_BASE_URL)
    response = client.chat.completions.create(
        model=settings.LLM_MODEL,
        messages=[
            {"role": "system", "content": REWRITE_SYSTEM_PROMPT},
            {"role": "user", "content": f"Заголовок: {title}\n\nТекст:\n{body}"},
        ],
        temperature=settings.LLM_TEMPERATURE,
    )
    text = response.choices[0].message.content or ""
    logger.info("Рерайт получен от модели %s, %d символов", settings.LLM_MODEL, len(text))
    return text, None
