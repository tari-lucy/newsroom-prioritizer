"""Фактчек рерайта: LLM сверяет переписанный текст с исходником и указывает расхождения фактов.

Без ключа LLM возвращает None (фактчек недоступен). Промпт вынесен отдельной константой —
его удобно дорабатывать под редакцию.
"""
import logging
from typing import Optional

from database.config import get_settings

logger = logging.getLogger(__name__)

# Промпт фактчекера. Правится под требования редакции.
FACTCHECK_PROMPT = """Ты — придирчивый фактчекер новостной редакции. Сверь ПЕРЕПИСАННЫЙ текст с ИСХОДНЫМ и найди фактические искажения. Сверяешь ТОЛЬКО с исходником — интернет и внешние источники не используешь.

Как проверять:
- Разбей переписанный текст на отдельные утверждения (каждое число, дата, имя, должность, место, цитата, причинно-следственная связь — отдельно).
- Каждое сверь с исходником: подтверждается / искажено / добавлено (нет в исходнике) / потеряно (важное упущено).
- Отдельно лови смысловые оттенки: слова, меняющие тональность, масштаб или смысл («пострадал» → «погиб», «планируют» → «решили»).

Правила:
- Оценивай только факты, не стиль. Цитаты должны совпадать дословно.
- Не выдумывай проблемы там, где их нет, и не додумывай факты сам.

Формат ответа (по-русски, кратко):
- Если всё точно — ответь одной строкой: «✅ Факты сохранены».
- Иначе — короткий список проблем, по одной на строку: «⚠ было → стало» или «❌ добавлено / потеряно: …».
- В конце — одна строка с итогом: «Точность: N/10»."""


def check_facts(title: str, body: str, rewrite: str) -> Optional[str]:
    """Возвращает вердикт фактчека или None, если LLM недоступен."""
    settings = get_settings()
    if not settings.LLM_API_KEY:
        return None

    from openai import OpenAI

    client = OpenAI(api_key=settings.LLM_API_KEY, base_url=settings.LLM_BASE_URL)
    original = f"{title}\n\n{body}".strip()
    response = client.chat.completions.create(
        model=settings.LLM_MODEL,
        messages=[
            {"role": "system", "content": FACTCHECK_PROMPT},
            {"role": "user", "content": f"ИСХОДНЫЙ:\n{original}\n\n---\n\nПЕРЕПИСАННЫЙ:\n{rewrite}"},
        ],
        temperature=0.0,
    )
    verdict = (response.choices[0].message.content or "").strip()
    return verdict or None
